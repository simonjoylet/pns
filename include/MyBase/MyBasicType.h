#ifndef MY_BASIC_TYPE_H
#define MY_BASIC_TYPE_H

/**
* 这里定义基本数据类型，以适应不同的平台。
*/

typedef void Void;
typedef bool Bool;
typedef char Char;
typedef unsigned char Uchar;
typedef short Short;
typedef unsigned short Ushort;
typedef int Int;
typedef unsigned int Uint;
typedef __int64 Long;
typedef unsigned __int64 Ulong;

#endif//MY_BASIC_TYPE_H
